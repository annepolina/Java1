    private void btnComputeGr4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnComputeGr4ActionPerformed
        // TODO add your handling code here:
        
        //Validate if the textfield is empty
        amountPaidGr4 = Double.parseDouble(txtAmtPdGr4.getText());
        totalAmountGr4 = Double.parseDouble(txtTotAmtGr4.getText());
        if (amountPaidGr4 < totalAmountGr4) {
            
            //Get input from the textfield and store them in the variables
            JOptionPane.showMessageDialog( null,"Please Enter Sufficient Amount!");
            txtAmtPdGr4.requestFocus();
            
        }
        else{
            //Create the instance of compute class
           McdoTOTALS_Gr4 Change = new McdoTOTALS_Gr4(totalAmountGr4, amountPaidGr4);
           totalChangeGr4 = Change.computeChangeGr4();
           
           txtChangeGr4.setText(String.valueOf(totalChangeGr4));
        }
        
    }//GEN-LAST:event_btnComputeGr4ActionPerformed

